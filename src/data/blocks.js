/**
 * バージョンを比較
 * @param {string} v1 - バージョン1 (例: "1.17")
 * @param {string} v2 - バージョン2 (例: "1.16")
 * @returns {number} v1 > v2 なら正、v1 < v2 なら負、等しければ0
 */
export function compareVersions(v1, v2) {
  const parts1 = v1.split('.').map(Number);
  const parts2 = v2.split('.').map(Number);

  for (let i = 0; i < Math.max(parts1.length, parts2.length); i++) {
    const p1 = parts1[i] || 0;
    const p2 = parts2[i] || 0;
    if (p1 !== p2) return p1 - p2;
  }
  return 0;
}

/**
 * 指定バージョンで利用可能なブロックをフィルタリング
 * @param {string} version - Minecraftバージョン (例: "1.21")
 * @returns {Array} 利用可能なブロックの配列
 */
export function getBlocksForVersion(version) {
  return ALL_BLOCKS.filter(block => compareVersions(version, block.minVersion) >= 0);
}

/**
 * カテゴリ別にブロックを取得
 * @param {string} version - Minecraftバージョン
 * @param {string} category - カテゴリID
 * @returns {Array} ブロックの配列
 */
export function getBlocksByCategory(version, category) {
  const availableBlocks = getBlocksForVersion(version);
  if (category === 'all') return availableBlocks;
  return availableBlocks.filter(block => block.category === category);
}

/**
 * ブロックを検索
 * @param {string} version - Minecraftバージョン
 * @param {string} query - 検索クエリ
 * @returns {Array} マッチするブロックの配列
 */
export function searchBlocks(version, query) {
  const availableBlocks = getBlocksForVersion(version);
  const lowerQuery = query.toLowerCase();
  return availableBlocks.filter(block =>
    block.id.toLowerCase().includes(lowerQuery) ||
    block.name.toLowerCase().includes(lowerQuery)
  );
}

